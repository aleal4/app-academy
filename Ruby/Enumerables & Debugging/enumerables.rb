class Array 
  def my_each(&prc)
    self.length.times do |i|
      prc.call(self[i])
    end

    self
  end

  def my_select(&prc)
    selected = []

    self.my_each do |i|
      selected << i if prc.call(i) == true
    end

    selected
  end
end







